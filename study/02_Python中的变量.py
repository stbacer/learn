"""
演示Python中变量的相关操作
"""

# 定义一个变量，用来记录钱包的余额
money = 20
# 通过print语句，输出变量记录的内容
print("钱包还有：",money) # 输出多份数据由“,”逗号隔开
# 买了一个冰淇淋，花费3元
money = money - 3
print("买了冰淇淋花费3元，还剩：", money,"元")

# 假设 每隔一小时，输出一下钱包的余额
print("现在时上午7点，钱包余额剩余：", money, "元")
print("现在时上午8点，钱包余额剩余：", money, "元")
print("现在时上午9点，钱包余额剩余：", money, "元")
print("现在时上午10点，钱包余额剩余：", money, "元")