bool_2 = False
print(f"bool_1变量的内容是：{bool_1},类型是：{type(bool_1)}")
print(f"bool_2变量的内容是：{bool_2},类型是：{type(bool_2)}")
print("------------")

# 比较运算符的使用
# ==, !=, >, <, >=, <=
# 演示
num1 = 10
num2 = 10
print(f"10 == 10的结果是：{num1 == num2}")

num1 = 12
num2 = 10
print(f"12 != 10的结果是：{num1 != num2}")

# 字符串之间的比较
name1 = "Python"
name2 = "爬虫"
print(f"Python == 爬虫 的结果是：{name1 == name2}")
print("---------")

# 演示大于 小于 小于等于 大于等于的比较结果
num1 = 10
num2 = 5
print(f"10 > 5的结果是：{num1 > num2}")
print(f"10 < 5的结果是：{num1 < num2}")
print("---------")

num1 = 10
num2 = 11
print(f"10 >= 11的结果是：{num1 >= num2}")
print(f"10 <= 11的结果是：{num1 <= num2}")