# 单引号定义法
name = 'Python'
print(type(name))

# 双引号定义法
name = "Python"
print(type(name))

# 三引号定义法
name = """Python"""
print(type(name))

# 在字符串内包含双引号
name = '"Python"'
print(name)
# 在字符串内包含单引号
name = "'Python'"
print(name)

# 使用转义字符 \ 解除引号的效用
name = "\"Python\""
print(name)

name = '\'python\''
print(name)
